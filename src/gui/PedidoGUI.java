    private void jTextFieldQuantidadeFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_jTextFieldQuantidadeFocusLost
        try {
            int x = Integer.parseInt(jTextFieldQuantidade.getText());
        if(x == 0) {
            JOptionPane.showMessageDialog(null, "Campo quantidade não pode ser zero!!!","Erro de preenchimento!!!", 0, new ImageIcon("Imagens/cancelar.png"));
            jTextFieldQuantidade.setText("");
            jTextFieldQuantidade.requestFocus();
        }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Campo quantidade dever ter um número inteiro!!!","Erro de preenchimento!!!", 0, new ImageIcon("Imagens/cancelar.png"));
            jTextFieldQuantidade.setText("");
            jTextFieldQuantidade.requestFocus();
        }
    }//GEN-LAST:event_jTextFieldQuantidadeFocusLost

