    private void jButtonAcrescentarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAcrescentarActionPerformed
        if(PedidoC.verificaItens(jTextFieldValor.getText(), jTextFieldQuantidade.getText(), jTextFieldCodigoItem.getText(), jComboBoxSelecionar.getSelectedItem().toString())) {
            double Total = Double.parseDouble(jTextFieldValor.getText()) * Integer.parseInt(jTextFieldQuantidade.getText());


            Modelo.addRow(new Object[] {jTextFieldCodigoItem.getText(),
            jComboBoxSelecionar.getSelectedItem(), jTextFieldValor.getText(),
            jTextFieldQuantidade.getText(), FormatoDecimal.format(Total).replace(",", ".")});
            limpaItens();
        }
    }//GEN-LAST:event_jButtonAcrescentarActionPerformed

    private void jComboBoxPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBoxPesquisarActionPerformed
        if(!(jComboBoxPesquisar.getSelectedItem() == null)) {
            String Cod = jComboBoxPesquisar.getSelectedItem().toString();
            Cod = Cod.substring(0, Cod.indexOf(" "));
            ClienteB = ClienteC.controlePreenchimento(Integer.parseInt(Cod));
            jTextFieldCodigo.setText(ClienteB.getCli_Codigo() + "");
            jTextFieldNome.setText(ClienteB.getCli_Nome() + "");
            jTextFieldRua.setText(ClienteB.getCli_Rua() + "");
            jTextFieldBairro.setText(ClienteB.getCli_Bairro() + "");
            jFormattedTextFieldTelefone.setText(ClienteB.getCli_Telefone() + "");
            jTextFieldData.setText(ClienteB.getCli_DataCad() + "");

        }
    }//GEN-LAST:event_jComboBoxPesquisarActionPerformed

