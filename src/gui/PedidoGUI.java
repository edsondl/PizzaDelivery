    private void jButtonCalcularActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonCalcularActionPerformed
        double TotalDoPedido = 0;
        for(int i = 0; i < Tabela.getRowCount(); i++) {
            TotalDoPedido += Double.parseDouble(Modelo.getValueAt(i, 4).toString());
        }
        jTextFieldTotal.setText(FormatoDecimal.format(TotalDoPedido).replace(",", "."));
        populaBeans();
        if(TotalDoPedido > 0 ) {
            jButtonFinalizar.setEnabled(true);
            jButtonAcrescentar.setEnabled(false);
            jButtonRemover.setEnabled(false);
            jButtonCalcular.setEnabled(false);
        }
    }//GEN-LAST:event_jButtonCalcularActionPerformed

